package com.changtai.RFID;

@SuppressWarnings("all")
public class UserFromCardBean {

    public String flag;
    public String stationNo;
    public String userCardNo;
    public String deviceNo;
    public String toal;
    public String overdeRaft;
    public String alarmValue;
    public String purchaseDate;

    public String getStationNo() {
        return stationNo;
    }

    public void setStationNo(String stationNo) {
        this.stationNo = stationNo;
    }

    public String getFlag() {
        return flag;
    }

    public void setFlag(String flag) {
        this.flag = flag;
    }

    public String getUserCardNo() {
        return userCardNo;
    }

    public void setUserCardNo(String userCardNo) {
        this.userCardNo = userCardNo;
    }

    public String getDeviceNo() {
        return deviceNo;
    }

    public void setDeviceNo(String deviceNo) {
        this.deviceNo = deviceNo;
    }

    public String getToal() {
        return toal;
    }

    public void setToal(String toal) {
        this.toal = toal;
    }

    public String getOverdeRaft() {
        return overdeRaft;
    }

    public void setOverdeRaft(String overdeRaft) {
        this.overdeRaft = overdeRaft;
    }

    public String getAlarmValue() {
        return alarmValue;
    }

    public void setAlarmValue(String alarmValue) {
        this.alarmValue = alarmValue;
    }

    public String getPurchaseDate() {
        return purchaseDate;
    }

    public void setPurchaseDate(String purchaseDate) {
        this.purchaseDate = purchaseDate;
    }
}
